def generate_couple_report(analysis: dict) -> bytes:
    """Generate complete SOW PDF dossier and return raw bytes."""
    pdf = SOWReport()
    pdf.set_auto_page_break(auto=True, margin=18)

    steps = [
        ("Cover", _cover),
        ("Health", _health),
        ("Tax", _tax),
        ("Investments", _investments),
        ("Goals", _goals),
        ("Insurance", _insurance),
        ("Market", _market),
        ("Actions", _actions),
    ]

    for name, func in steps:
        try:
            func(pdf, analysis)
        except Exception as e:
            # Fallback if a page fails to render
            print(f"PDF Block '{name}' failed: {e}")
            pdf.add_page()
            pdf.set_font("Helvetica", "I", 10)
            pdf.set_text_color(180, 0, 0)
            pdf.cell(0, 10, f"[Data rendering error in {name} section - Proceeding...]")

    buf = io.BytesIO()
    pdf.output(buf)
    return buf.getvalue()
