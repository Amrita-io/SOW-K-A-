def _safe(text: str) -> str:
    """Robustly strip non-Latin-1 chars and substitute complex symbols to prevent PDF crashes."""
    if not isinstance(text, str):
        text = str(text)
    # Map high-Unicode characters to their standard Latin-1 equivalents
    replacements = {
        '₹': 'Rs.',     # Indian Rupee symbol
        '\u20b9': 'Rs.',# Unicode Rupee
        '\u2014': '-',  # em-dash
        '\u2013': '-',  # en-dash
        '\u2018': "'",  # curly single quote
        '\u2019': "'",  # curly single quote
        '\u201c': '"',  # curly double quote
        '\u201d': '"',  # curly double quote
    }
    for char, rep in replacements.items():
        text = text.replace(char, rep)
    return text.encode('latin-1', errors='replace').decode('latin-1')


def _fmt(amount) -> str:
    """Indian number format without currency prefix."""
    try:
        amount = int(float(amount or 0))
    except Exception:
        return "0"
    if amount < 0:
        return f"-{_fmt(-amount)}"
    s = str(amount)
    if len(s) <= 3:
        return s
    result = s[-3:]
    s = s[:-3]
    while s:
        result = s[-2:] + "," + result
        s = s[:-2]
    return result


def _inr(amount) -> str:
    return f"Rs {_fmt(amount)}"


class SOWReport(FPDF):
    """SOW branded PDF class."""

    def header(self):
        if self.page_no() == 1:
            return
        self.set_fill_color(*DARK)
        self.rect(0, 0, 210, 10, 'F')
        self.set_font("Helvetica", "B", 7)
        self.set_text_color(*GOLD)
        self.set_xy(10, 2)
        self.cell(150, 6, _safe("SOW - Strategy Optimization Workspace"), align="L")
        self.set_text_color(*WHITE)
        self.set_xy(140, 2)
        self.cell(60, 6, f"Page {self.page_no()}", align="R")
        self.ln(10)

    def footer(self):
        self.set_y(-12)
        self.set_font("Helvetica", "I", 7)
        self.set_text_color(*MID)
        self.cell(0, 6, "Generated by SOW Intelligence Engine  |  CONFIDENTIAL", align="C")

    def gold_bar(self, title: str, subtitle: str = ""):
        """Full-width golden section header."""
        y = self.get_y()
        self.set_fill_color(*GOLD)
        self.rect(10, y, 190, 11, 'F')
        self.set_xy(14, y)
        self.set_font("Helvetica", "B", 10)
        self.set_text_color(*DARK)
        self.cell(0, 11, _safe(title.upper()), new_x="LMARGIN", new_y="NEXT")
        if subtitle:
            self.set_font("Helvetica", "I", 8)
            self.set_text_color(*MID)
            self.cell(0, 5, _safe(subtitle), new_x="LMARGIN", new_y="NEXT")
        self.ln(3)

    def kv(self, label: str, value: str, val_bold=False, val_color=None):
        """Key-value row."""
        self.set_x(12)
        self.set_font("Helvetica", "", 9)
        self.set_text_color(*MID)
        self.cell(85, 7, _safe(label))
        self.set_font("Helvetica", "B" if val_bold else "", 9)
        self.set_text_color(*(val_color or DARK))
        self.cell(0, 7, _safe(value), new_x="LMARGIN", new_y="NEXT")

    def row_cells(self, cols, widths, bold=False, header=False):
        """Table row."""
        if header:
            self.set_fill_color(*LIGHT_BG)
            self.rect(10, self.get_y(), 190, 8, 'F')
        self.set_x(12)
        for i, (text, w) in enumerate(zip(cols, widths)):
            self.set_font("Helvetica", "B" if (bold or header) else "", 8)
            self.set_text_color(*MID if header else DARK)
            self.cell(w, 8, _safe(str(text))[:38], align="R" if i > 0 else "L")
        self.ln(8)

    def rule(self):
        """Thin horizontal divider."""
        self.ln(2)
        self.set_draw_color(*BORDER)
        self.line(10, self.get_y(), 200, self.get_y())
        self.ln(4)

    def stat_box(self, x, y, w, h, label, value, col=None):
        """Small metric box."""
        self.set_fill_color(*LIGHT_BG)
        self.set_draw_color(*BORDER)
        self.rect(x, y, w, h, 'FD')
        self.set_xy(x + 2, y + 2)
        self.set_font("Helvetica", "B", 6)
        self.set_text_color(*MID)
        self.cell(w - 4, 5, _safe(label.upper())[:20], align="C")
        self.set_xy(x + 2, y + 7)
        self.set_font("Helvetica", "B", 10)
        self.set_text_color(*(col or DARK))
        self.cell(w - 4, 7, _safe(str(value))[:16], align="C")


